# 演算子
# + - * / // % **

x = 10
print(x / 3) # 3.33... （商）
print(x // 3) # 3 （）
print(x % 3) # 1 （余り）
print(x ** 2) # 100 （二乗）

y = 4
# y = y + 12
y += 12
print(y) # 16

# and or not
print(True and False) # False
print(True or False) # True
print(not True) # False

# + *
print("hello " + "world")
print("hello " * 3)
